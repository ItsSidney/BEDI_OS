    for (int i = 0; i < MAX_FILES; i++) {
        if (files[i].in_use && files[i].parent_dir == current_dir) {
            int match = 1;
            int j = 0;
            while (filename[j] != 0 && files[i].name[j] != 0) {
                if (filename[j] != files[i].name[j]) {
                    match = 0;
                    break;
                }
                j++;
            }
            if (match && filename[j] == 0 && files[i].name[j] == 0) {
                return 1;
            }
        }
    }
