int fs_create(const char* filename) {
    if (fs_exists(filename)) {
        return -1; // File already exists
    }
    
    for (int i = 0; i < MAX_FILES; i++) {
        if (!files[i].in_use) {
            files[i].in_use = 1;
            files[i].size = 0;
            files[i].type = FS_FILE;
            files[i].parent_dir = current_dir;
            files[i].flags = 0;
            extern uint32_t timer_get_ms(void);
            files[i].creation_time = timer_get_ms();
            files[i].modified_time = files[i].creation_time;
            int j = 0;
            while (filename[j] != 0 && j < MAX_FILENAME - 1) {
                files[i].name[j] = filename[j];
                j++;
            }
            files[i].name[j] = 0;
            return i; // Return file descriptor
        }
    }
    return -1; // No free file slots
}

int fs_touch(const char* filename) {
    if (fs_exists(filename)) {
        for (int i = 0; i < MAX_FILES; i++) {
            if (files[i].in_use && files[i].parent_dir == current_dir && files[i].type == FS_FILE) {
                int match = 1, j = 0;
                while (filename[j] && files[i].name[j]) {
                    if (filename[j] != files[i].name[j]) { match = 0; break; }
                    j++;
                }
                if (match && !filename[j] && !files[i].name[j]) {
                    extern uint32_t timer_get_ms(void);
                    files[i].modified_time = timer_get_ms();
                    return 0;
                }
            }
        }
        return -1;
    }
    return fs_create(filename);
}

